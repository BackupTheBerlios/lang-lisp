<h2>Lang(working title) project homepage</h2>
This is the home page of the lang project. It is very early in development, i even put todos 
in the website. Currently the website is not designed to attract anyone in particular. Please don't 
advertise this language in a way to suggest that it anywhere near ready to be used for actual programming.
<a href="https://developer.berlios.de/projects/lang-lisp/">Here</a> is the Berlios project page.

<h3>What Lang is intended to be</h3>
I intend to make a lispy programming language, that treats types better then 
other lisps. I will also try to make libraries able to give the user access to 
many programming methods and concepts. <a href="Explanation.html">Read further
 explanation</a> for a more detailed description.(And some considerations.)

<h3>Stages of development</h3>
Currently i am developing the core. Here are <a href="Stages.html">more stages and milestones</a>.

<h3>Course of action by you</h3>
<ul>
  <li>If you're not interested in developing languages. Please come back later :), maybe there will
be an interesting new language to learn.</li>
  <li>If you're knowledgable enough, and interested in suggesting something, read
 the <a href="Explanation.html">Explanation</a>, look in the
 <a href="https://developer.berlios.de/forum/?group_id=10613">forum</a> for suggestions already 
there and post in the forum. The purpose of suggesting is to see if the requested feature 
can/should be implemented in the language, and how. I mainly hope to find features that
can not be written with a library with the the core language as-is, so i can change the core 
language so that it can. When it can be written as a language, the </li>
  <li>If you're interested, knowledgeable, skilled, and want to help develop, you can also post in
the forum. The problem is that i do not know how to make co-operation work at this point. Note
that brevity, consistency, simplicity and all that of code is paramount. Also, the project can be 
paralelized more in later stages. The symbol tree is one thing that can be worked on in
 parallel.(It could be done as a separate project.) Another thing that could be very useful,
